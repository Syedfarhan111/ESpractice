const AddNumberOpt=(num1,num2) => num1+num2;
const subNumberOpt=(num1,num2) => num1-num2;
const multNumberOpt=(num1,num2) => num1*num2;



console.log("****************************************Addition******************************************************");

console.log(AddNumberOpt(20,30));
console.log("sum of given no" +AddNumberOpt(30,60));
console.log("sum of 10 and 15 is" +AddNumberOpt(10,15));
console.log(`############
sum of 
50+50= ${AddNumberOpt(30,60)}
##############`);

console.log("**************************************Substraction********************************************************");

console.log(subNumberOpt(20,30));
console.log("sum of given no" +subNumberOpt(30,60));
console.log("sum of 10 and 15 is" +subNumberOpt(10,15));
console.log(`############
sum of 
50+50= ${subNumberOpt(30,60)}
##############`);
console.log("*****************************************Multiplication*****************************************************");
console.log(multNumberOpt(20,30));
console.log("sum of given no" +multNumberOpt(30,60));
console.log("sum of 10 and 15 is" +multNumberOpt(10,15));
console.log(`############
sum of 
50+50= ${multNumberOpt(30,60)}
##############`);



